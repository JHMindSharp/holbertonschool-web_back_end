#!/usr/bin/env python3
"""
This module provides a function to compute the
floor of a floating-point number.
"""

import math


def floor(n: float) -> int:
    """
    Calculate the floor of a floating-point number.

    Args:
        n (float): A floating-point number.

    Returns:
        int: The floor of the number.
    """
    return math.floor(n)
